	public static String generateUTF(int start, int end) {
		char[] b = new char[end-start];
		for(int i = 0; i < b.length; i++)
			b[i] = (char) (i+start);
		return new String(b);
	}
	public static String generateUTF(int...vals) {
		char[] b = new char[vals.length];
		for(int i = 0; i < b.length; i++)
			b[i] = (char) (vals[i]);
		return new String(b);
	}
